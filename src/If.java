public class If {
    public static void main(String[] args) {
        int a = 1;

        if(a == 5) {
            System.out.println("To jest 5 !!!");
        } else if(a == 5 && a > 33) {
            System.out.println("To jest 6 !!!");
        } else if(a == 7) {
            System.out.println("To jest 7 !!!");
        } else {
            System.out.println("Nie wiadomo co to jest !!!");
        }


        System.out.println("Koniec programu !!");
    }
}
